var month = 5;

switch(month){
    case 12:
    case 1:
    case 2:
    console.log('winter');
    break;

    case 3:
    case 4:
    case 5:
    console.log('spring');
    break;

    case 6:
    case 7:
    case 8:
    console.log('summer');
    break;

    case 9:
    case 10:
    case 11:
    console.log('autumn');
    break;

    default:
    console.log('It is not valid season');
    break;

}